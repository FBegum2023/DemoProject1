package fatema.JavaPractices;

public class DataTypeDeclaration {
	static double num;
	
	public static void main(String[] args) {

		// Variable another name is declaration and value is intialization

		// 1 2 3
		//data types-> String, name -> variable(container), "Smart Tech" -> Value
		String name = "!@$#@*Y$*&!$!HBFUYBWUHB WUEBFu";
		System.out.println("The name of String is "+name+".");

		//data type-> int, number -> variable(container), 10 -> Value		

		int number = 20;
		double nums=5.5;
		double sum=(number+nums);
		System.out.println("The sum of 2 numbers are; "+sum+".");

		//data types-> double, num -> variable(container), 10.99 -> Value			

		System.out.println("The double number is "+num+".");
		String money = "$10.99";
		System.out.println("The value of the string is"+money+".");
		
		//char can handle anything within single quotation only 1 character 
		char single = '$';
		System.out.println("The character is "+single+".");

		boolean age = true; // t or f
		//int age =30;
		System.out.println("Age is "+age+".");

	}


}
