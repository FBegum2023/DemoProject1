package com.abstractionFatema;

public abstract class AbstructClassA {
	public abstract void getToyota();
	
	public abstract void getHonda();
	
	public abstract void getNissan();
			
	
	protected void getSalary() {
		System.out.println("My salary is $10000.");
	}

	protected void getHouse() {
		System.out.println("This is my house.");
	}

	protected void getBusiness() {
		System.out.println("Family Business");
	}
	
}

    
